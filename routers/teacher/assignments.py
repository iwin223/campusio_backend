@router.delete("/{assignment_id}", response_model=dict)
async def delete_assignment(
    assignment_id: str,
    current_user: User = Depends(require_roles(UserRole.TEACHER)),
    session: AsyncSession = Depends(get_session),
):
    """Delete an assignment (only by the teacher who created it)."""
    try:
        school_id = current_user.school_id
        
        # Get staff record
        staff_result = await session.execute(
            select(Staff).where(Staff.user_id == current_user.id)
        )
        staff = staff_result.scalar_one_or_none()
        if not staff:
            raise HTTPException(status_code=404, detail="Staff profile not found")
        
        # Get assignment
        assignment_result = await session.execute(
            select(Assignment).where(
                Assignment.id == assignment_id,
                Assignment.school_id == school_id
            )
        )
        assignment = assignment_result.scalar_one_or_none()
        
        if not assignment:
            raise HTTPException(status_code=404, detail="Assignment not found")
        
        # Verify ownership - check if staff is the teacher of this assignment
        if assignment.teacher_id != staff.id:
            raise HTTPException(status_code=403, detail="You can only delete your own assignments")
        
        # Delete associated questions first
        questions_result = await session.execute(
            select(AssignmentQuestion).where(
                AssignmentQuestion.assignment_id == assignment_id
            )
        )
        questions = questions_result.scalars().all()
        for question in questions:
            await session.delete(question)
        
        # Delete the assignment
        await session.delete(assignment)
        await session.commit()
        
        return {
            "message": "Assignment deleted successfully",
            "assignment_id": assignment_id,
        }
        
    except HTTPException:
        raise
    except Exception as e:
        await session.rollback()
        raise HTTPException(status_code=500, detail=f"Error deleting assignment: {str(e)}")


@router.post("/{assignment_id}/publish", response_model=dict)
async def publish_assignment(
    assignment_id: str,
    current_user: User = Depends(require_roles(UserRole.TEACHER)),
    session: AsyncSession = Depends(get_session),
):
    """
    Publish an assignment (moves from DRAFT to PUBLISHED).
    Published assignments become visible to students in their portal.
    """
    try:
        school_id = current_user.school_id
        
        # Get staff record
        staff_result = await session.execute(
            select(Staff).where(Staff.user_id == current_user.id)
        )
        staff = staff_result.scalar_one_or_none()
        if not staff:
            raise HTTPException(status_code=404, detail="Staff profile not found")
        
        # Get assignment
        assignment_result = await session.execute(
            select(Assignment).where(
                Assignment.id == assignment_id,
                Assignment.school_id == school_id
            )
        )
        assignment = assignment_result.scalar_one_or_none()
        
        if not assignment:
            raise HTTPException(status_code=404, detail="Assignment not found")
        
        # Verify ownership - check if staff is the teacher of this assignment
        if assignment.teacher_id != staff.id:
            raise HTTPException(status_code=403, detail="You can only publish your own assignments")
        
        # Check if assignment is in DRAFT status
        if assignment.status != AssignmentStatus.DRAFT:
            raise HTTPException(
                status_code=400,
                detail=f"Can only publish DRAFT assignments. Current status: {assignment.status}"
            )
        
        # Publish the assignment
        assignment.status = AssignmentStatus.PUBLISHED
        assignment.published_date = datetime.utcnow()
        assignment.updated_at = datetime.utcnow()
        
        session.add(assignment)
        await session.commit()
        
        return {
            "message": "Assignment published successfully! Students can now see it.",
            "assignment_id": assignment_id,
            "status": assignment.status,
            "published_date": assignment.published_date.isoformat() if assignment.published_date else None,
        }
        
    except HTTPException:
        raise
    except Exception as e:
        await session.rollback()
        raise HTTPException(status_code=500, detail=f"Error publishing assignment: {str(e)}")
