@router.post("", response_model=dict)
async def create_assignment(
    assignment_data: dict,
    current_user: User = Depends(require_roles(UserRole.TEACHER)),
    session: AsyncSession = Depends(get_session),
):
    """
    Create a new assignment for a class.
    Teachers can only create assignments for classes they teach.
    
    Body:
    {
        "class_id": int,
        "subject_id": int,
        "title": str,
        "description": str,
        "due_date": "YYYY-MM-DD",
        "total_points": float (optional, default 100),
        "instructions": str (optional),
    }
    """
    try:
        school_id = current_user.school_id
        
        # Get the Staff record for this user
        staff_result = await session.execute(
            select(Staff).where(Staff.user_id == current_user.id)
        )
        staff = staff_result.scalar()
        
        # Fallback: try to match by email and school
        if not staff and current_user.school_id:
            staff_result = await session.execute(
                select(Staff).where(
                    and_(
                        Staff.email == current_user.email,
                        Staff.school_id == current_user.school_id
                    )
                )
            )
            staff = staff_result.scalar()
        
        if not staff:
            raise HTTPException(
                status_code=404,
                detail="Staff profile not found. Please ensure your portal credentials have been generated by the admin."
            )
        
        staff_id = staff.id
        
        # Verify teacher is assigned to teach this class/subject
        assignment_result = await session.execute(
            select(TeacherAssignment).where(
                and_(
                    TeacherAssignment.school_id == school_id,
                    TeacherAssignment.staff_id == staff_id,
                    TeacherAssignment.class_id == assignment_data.get("class_id"),
                    TeacherAssignment.subject_id == assignment_data.get("subject_id"),
                )
            )
        )
        assignment = assignment_result.scalar()
        
        if not assignment:
            raise HTTPException(
                status_code=403,
                detail="You are not assigned to teach this class/subject"
            )
        
        # Verify class and subject exist
        class_result = await session.execute(
            select(Class).where(Class.id == assignment_data.get("class_id"))
        )
        class_obj = class_result.scalar()
        
        if not class_obj:
            raise HTTPException(status_code=404, detail="Class not found")
        
        subject_result = await session.execute(
            select(Subject).where(Subject.id == assignment_data.get("subject_id"))
        )
        subject = subject_result.scalar()
        
        if not subject:
            raise HTTPException(status_code=404, detail="Subject not found")
        
        # Create assignment record in database
        assignment_id = str(uuid.uuid4())
        
        # Parse due_date if it's a string
        due_date = assignment_data.get("due_date")
        if due_date and isinstance(due_date, str):
            from datetime import datetime as dt
            due_date = dt.fromisoformat(due_date.replace('Z', '+00:00')).date()
        
        # Create rubric/grading settings JSON if provided
        rubric_data = None
        if assignment_data.get("grading_settings"):
            rubric_data = json.dumps(assignment_data.get("grading_settings"))
        
        assignment = Assignment(
            id=assignment_id,
            school_id=school_id,
            teacher_id=staff_id,
            class_id=assignment_data.get("class_id"),
            subject_id=assignment_data.get("subject_id"),
            academic_term_id=assignment_data.get("academic_term_id", "1"),
            title=assignment_data.get("title"),
            description=assignment_data.get("description", ""),
            assignment_type=assignment_data.get("assignment_type", "classwork"),
            status=AssignmentStatus.DRAFT,
            instructions=assignment_data.get("instructions", ""),
            rubric=rubric_data,
            points_possible=assignment_data.get("points_possible", 100),
            due_date=due_date,
            recorded_by=current_user.id,
            created_at=datetime.utcnow(),
            updated_at=datetime.utcnow(),
            created_date=datetime.utcnow()
        )
        
        session.add(assignment)
        await session.commit()
        
        # Save questions if provided
        if assignment_data.get("questions"):
            for q_data in assignment_data.get("questions"):
                question = AssignmentQuestion(
                    id=str(uuid.uuid4()),
                    school_id=school_id,
                    assignment_id=assignment_id,
                    question_text=q_data.get("question"),
                    question_type=q_data.get("type", "essay"),
                    options=json.dumps(q_data.get("options", [])) if q_data.get("options") else None,
                    correct_answer=q_data.get("answer"),
                    points=1.0,
                    created_at=datetime.utcnow(),
                    updated_at=datetime.utcnow()
                )
                session.add(question)
            await session.commit()
        
        return {
            "message": "Assignment created successfully",
            "assignment_id": assignment_id,
            "title": assignment_data.get("title"),
            "class_id": assignment_data.get("class_id"),
            "subject_id": assignment_data.get("subject_id"),
            "due_date": due_date.isoformat() if due_date else None,
            "created_by": staff_id,
            "created_at": datetime.utcnow().isoformat(),
            "questions_count": len(assignment_data.get("questions", []))
        }
        
    except HTTPException:
        raise
    except Exception as e:
        await session.rollback()
        raise HTTPException(status_code=500, detail=f"Error creating assignment: {str(e)}")


@router.get("/my-assignments", response_model=dict)
async def get_my_assignments(
    class_id: Optional[str] = None,
    subject_id: Optional[str] = None,
    page: int = Query(1, ge=1),
    limit: int = Query(20, ge=1, le=100),
    current_user: User = Depends(require_roles(UserRole.TEACHER)),
    session: AsyncSession = Depends(get_session),
):
    """
    Get all assignments created by the teacher.
    Optionally filter by class and/or subject.
    """
    try:
        school_id = current_user.school_id
        
        # Get the Staff record for this user
        staff_result = await session.execute(
            select(Staff).where(Staff.user_id == current_user.id)
        )
        staff = staff_result.scalar()
        
        # Fallback: try to match by email and school
        if not staff and current_user.school_id:
            staff_result = await session.execute(
                select(Staff).where(
                    and_(
                        Staff.email == current_user.email,
                        Staff.school_id == current_user.school_id
                    )
                )
            )
            staff = staff_result.scalar()
        
        if not staff:
            raise HTTPException(
                status_code=404,
                detail="Staff profile not found."
            )
        
        staff_id = staff.id
        
        # Build query for teacher's assignments
        query = select(Assignment).where(
            and_(
                Assignment.school_id == school_id,
                Assignment.teacher_id == staff_id,
            )
        )
        
        # Apply filters if provided
        if class_id:
            query = query.where(Assignment.class_id == class_id)
        if subject_id:
            query = query.where(Assignment.subject_id == subject_id)
        
        # Get total count
        count_result = await session.execute(
            select(func.count(Assignment.id)).where(
                and_(
                    Assignment.school_id == school_id,
                    Assignment.teacher_id == staff_id,
                )
            )
        )
        total = count_result.scalar() or 0
        
        # Apply pagination
        offset = (page - 1) * limit
        query = query.offset(offset).limit(limit)
        
        # Get assignments
        result = await session.execute(query)
        assignments = result.scalars().all()
        
        # Format response
        assignments_list = []
        for assignment in assignments:
            # Get class and subject names
            class_result = await session.execute(
                select(Class).where(Class.id == assignment.class_id)
            )
            cls = class_result.scalar()
            
            subject_result = await session.execute(
                select(Subject).where(Subject.id == assignment.subject_id)
            )
            subj = subject_result.scalar()
            
            assignments_list.append({
                "assignment_id": assignment.id,
                "id": assignment.id,
                "title": assignment.title,
                "description": assignment.description,
                "class_id": assignment.class_id,
                "class_name": cls.name if cls else "Unknown",
                "subject_id": assignment.subject_id,
                "subject_name": subj.name if subj else "Unknown",
                "assignment_type": assignment.assignment_type,
                "status": assignment.status,
                "due_date": assignment.due_date.isoformat() if assignment.due_date else None,
                "points_possible": assignment.points_possible,
                "total_points": assignment.points_possible,
                "created_at": assignment.created_at.isoformat() if assignment.created_at else None,
            })
        
        total_pages = (total + limit - 1) // limit if total > 0 else 0
        
        return {
            "items": assignments_list,
            "total": total,
            "page": page,
            "limit": limit,
            "pages": total_pages,
            "message": "Assignments retrieved successfully"
        }
        
    except HTTPException:
        raise
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Error retrieving assignments: {str(e)}")


@router.get("/{assignment_id}", response_model=dict)
async def get_assignment_details(
    assignment_id: str,
    current_user: User = Depends(require_roles(UserRole.TEACHER)),
    session: AsyncSession = Depends(get_session),
):
    """Get details of a specific assignment."""
    try:
        school_id = current_user.school_id
        
        # Get assignment details from database
        result = await session.execute(
            select(Assignment).where(
                and_(
                    Assignment.id == assignment_id,
                    Assignment.school_id == school_id
                )
            )
        )
        assignment = result.scalar()
        
        if not assignment:
            raise HTTPException(status_code=404, detail="Assignment not found")
        
        # Get questions
        questions_result = await session.execute(
            select(AssignmentQuestion).where(AssignmentQuestion.assignment_id == assignment_id)
        )
        questions = questions_result.scalars().all()
        
        # Format questions for response
        formatted_questions = []
        for q in questions:
            question_data = {
                "id": q.id,
                "question": q.question_text,
                "type": q.question_type,
                "answer": q.correct_answer,
                "points": q.points,
            }
            
            # Parse options if they exist
            if q.options:
                try:
                    if isinstance(q.options, str):
                        question_data["options"] = json.loads(q.options)
                    else:
                        question_data["options"] = q.options
                except:
                    question_data["options"] = []
            else:
                question_data["options"] = []
            
            formatted_questions.append(question_data)
        
        return {
            "assignment": {
                "id": assignment.id,
                "class_id": assignment.class_id,
                "subject_id": assignment.subject_id,
                "title": assignment.title,
                "description": assignment.description,
                "due_date": assignment.due_date.isoformat() if assignment.due_date else None,
                "total_points": assignment.points_possible,
                "instructions": assignment.instructions,
                "assignment_type": assignment.assignment_type,
                "status": assignment.status,
                "questions_count": len(questions),
                "questions": formatted_questions,
            },
            "message": "Assignment details retrieved successfully"
        }
        
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Error retrieving assignment: {str(e)}")


@router.get("/{assignment_id}/submissions", response_model=dict)
async def get_assignment_submissions(
    assignment_id: str,
    status: Optional[str] = None,
    page: int = Query(1, ge=1),
    limit: int = Query(50, ge=1, le=100),
    current_user: User = Depends(require_roles(UserRole.TEACHER)),
    session: AsyncSession = Depends(get_session),
):
    """
    Get all submissions for an assignment.
    Optionally filter by submission status (SUBMITTED, GRADED, MISSING, LATE).
    """
    try:
        school_id = current_user.school_id
        
        # Get the Staff record for this user
        staff_result = await session.execute(
            select(Staff).where(Staff.user_id == current_user.id)
        )
        staff = staff_result.scalar()
        
        # Fallback: try to match by email and school
        if not staff and current_user.school_id:
            staff_result = await session.execute(
                select(Staff).where(
                    and_(
                        Staff.email == current_user.email,
                        Staff.school_id == current_user.school_id
                    )
                )
            )
            staff = staff_result.scalar()
        
        if not staff:
            raise HTTPException(
                status_code=404,
                detail="Staff profile not found"
            )
        
        # Verify assignment exists and belongs to this teacher
        assignment_result = await session.execute(
            select(Assignment).where(
                Assignment.id == assignment_id,
                Assignment.school_id == school_id,
                Assignment.teacher_id == staff.id
            )
        )
        assignment = assignment_result.scalar_one_or_none()
        
        if not assignment:
            raise HTTPException(status_code=404, detail="Assignment not found")
        
        # Get class to find all students
        class_result = await session.execute(
            select(Class).where(Class.id == assignment.class_id)
        )
        cls = class_result.scalar_one_or_none()
        
        if not cls:
            raise HTTPException(status_code=404, detail="Class not found")
        
        # Get all students in this class
        students_result = await session.execute(
            select(Student).where(Student.class_id == assignment.class_id)
        )
        all_students = students_result.scalars().all()
        
        # Get submissions for this assignment
        query = select(Submission).where(
            Submission.assignment_id == assignment_id,
            Submission.school_id == school_id
        )
        
        # Filter by status if provided
        if status:
            query = query.where(Submission.status == status)
        
        # Get total count for pagination
        count_query = select(func.count(Submission.id)).where(
            Submission.assignment_id == assignment_id,
            Submission.school_id == school_id
        )
        if status:
            count_query = count_query.where(Submission.status == status)
        
        total_result = await session.execute(count_query)
        total = total_result.scalar() or 0
        
        # Apply pagination
        offset = (page - 1) * limit
        query = query.order_by(Submission.submission_date.desc()).offset(offset).limit(limit)
        
        submissions_result = await session.execute(query)
        submissions = submissions_result.scalars().all()
        
        # Format submissions with student info
        submissions_list = []
        submitted_student_ids = set()
        
        for submission in submissions:
            # Get student info
            student_result = await session.execute(
                select(Student).where(Student.id == submission.student_id)
            )
            student = student_result.scalar_one_or_none()
            
            if student:
                submitted_student_ids.add(student.id)
                submissions_list.append({
                    "id": submission.id,
                    "student_id": student.id,
                    "student_name": f"{student.first_name} {student.last_name}",
                    "submission_date": submission.submission_date.isoformat() if submission.submission_date else None,
                    "status": submission.status,
                    "score": submission.score,
                    "max_score": submission.max_score,
                    "feedback": submission.feedback,
                    "graded_date": submission.graded_date.isoformat() if submission.graded_date else None,
                    "graded_answers": json.loads(submission.graded_answers) if submission.graded_answers else None,
                    "submission_text": submission.submission_text
                })
        
        # Add missing submissions for students who haven't submitted
        missing_count = 0
        for student in all_students:
            if student.id not in submitted_student_ids:
                missing_count += 1
                # Only add to list if we're on the first page and there's room
                if page == 1 and len(submissions_list) < limit:
                    submissions_list.append({
                        "id": None,
                        "student_id": student.id,
                        "student_name": f"{student.first_name} {student.last_name}",
                        "submission_date": None,
                        "status": "NOT_SUBMITTED",
                        "score": None,
                        "max_score": assignment.points_possible,
                        "feedback": None,
                        "graded_date": None,
                        "submission_text": None
                    })
        
        # Calculate pages
        total_with_missing = total + missing_count if not status or status == "NOT_SUBMITTED" else total
        pages = (total_with_missing + limit - 1) // limit if total_with_missing > 0 else 1
        
        return {
            "items": submissions_list,
            "total": total_with_missing,
            "page": page,
            "limit": limit,
            "pages": pages,
            "message": f"Retrieved {len(submissions_list)} submissions"
        }
        
    except HTTPException:
        raise
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Error retrieving submissions: {str(e)}")


@router.post("/{assignment_id}/grade/{submission_id}", response_model=dict)
async def grade_submission(
    assignment_id: str,
    submission_id: str,
    grade_data: dict,
    current_user: User = Depends(require_roles(UserRole.TEACHER)),
    session: AsyncSession = Depends(get_session),
):
    """
    Grade a student submission.
    
    Body:
    {
        "score": float,
        "feedback": str (optional),
        "rubric_scores": dict (optional),
        "question_scores": dict (optional)  # {"question_id": score, ...}
    }
    """
    try:
        school_id = current_user.school_id
        
        # Get the Staff record for this user
        staff_result = await session.execute(
            select(Staff).where(Staff.user_id == current_user.id)
        )
        staff = staff_result.scalar()
        
        # Fallback: try to match by email and school
        if not staff and current_user.school_id:
            staff_result = await session.execute(
                select(Staff).where(
                    and_(
                        Staff.email == current_user.email,
                        Staff.school_id == current_user.school_id
                    )
                )
            )
            staff = staff_result.scalar()
        
        if not staff:
            raise HTTPException(
                status_code=404,
                detail="Staff profile not found."
            )
        
        # Get the submission
        submission_result = await session.execute(
            select(Submission).where(
                Submission.id == submission_id,
                Submission.assignment_id == assignment_id,
                Submission.school_id == school_id
            )
        )
        submission = submission_result.scalar_one_or_none()
        
        if not submission:
            raise HTTPException(status_code=404, detail="Submission not found")
        
        # Verify teacher owns this assignment
        assignment_result = await session.execute(
            select(Assignment).where(
                Assignment.id == assignment_id,
                Assignment.teacher_id == staff.id,
                Assignment.school_id == school_id
            )
        )
        assignment = assignment_result.scalar_one_or_none()
        
        if not assignment:
            raise HTTPException(status_code=403, detail="You do not have permission to grade this assignment")
        
        # Validate score
        score = grade_data.get("score")
        if score is not None:
            if score < 0 or score > (assignment.points_possible or 100):
                raise HTTPException(
                    status_code=400, 
                    detail=f"Score must be between 0 and {assignment.points_possible or 100}"
                )
        
        # Update the submission
        if score is not None:
            submission.score = float(score)
        if "feedback" in grade_data:
            submission.feedback = grade_data["feedback"]
        if "rubric_scores" in grade_data:
            submission.rubric_scores = json.dumps(grade_data["rubric_scores"])
        if "question_scores" in grade_data:
            submission.graded_answers = json.dumps(grade_data["question_scores"])
        
        submission.status = SubmissionStatus.GRADED
        submission.graded_date = datetime.utcnow()
        submission.graded_by = staff.id
        
        await session.commit()
        await session.refresh(submission)
        
        return {
            "message": "Submission graded successfully",
            "submission_id": submission_id,
            "score": submission.score,
            "max_score": submission.max_score,
            "feedback": submission.feedback,
            "graded_by": staff.id,
            "graded_at": submission.graded_date.isoformat(),
        }
        
    except HTTPException:
        raise
    except Exception as e:
        await session.rollback()
        raise HTTPException(status_code=500, detail=f"Error grading submission: {str(e)}")


